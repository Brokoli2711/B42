#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	char	null_byte;

	null_byte = '\0';
	while (*s)
	{
		write(fd, s, 1);
		s++;
	}
	write(fd, &null_byte, 1);
}
